local Object = require('Object')

-- -----------------------------------------------------------------------------
-- Class
-- -----------------------------------------------------------------------------

local ClassMT = {}
local Class = setmetatable({}, ClassMT)

-- -----------------------------------------------------------------------------
-- Constructor
-- -----------------------------------------------------------------------------

local InstanceMT = { __call = function(self, ...) return Object(self, ...) end }

function ClassMT:__call(instance)
  return setmetatable(instance or {}, InstanceMT)
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Class
